  func factorial(_ n: Int)-> Int {
      print("Calculating factorial(\(n))...")
      let occ = 1
    if n > 1{
        let x = factorial(n-1)
        let y = n * x
        print("Non-terminal case, returning \(y).")
        return n * x
    }
    else{
        print("Terminal case, returning 1.") 
        return occ
    }
}
  print("The final result is: \(factorial(n)).")

  
